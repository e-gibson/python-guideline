# while loop
number = 20
while number <= 25:
    print(number)
    number += 1

# decrement
num = 10
while num >= 1:
    print(num)
    num -= 1

# for loop
languages = ["python", "PHP", "kotlin", "Java"]
for x in languages:
    print(x)

# range
for z in range(30, 36):
    print(z)

for y in range(1, 10, 2):
    print(y)